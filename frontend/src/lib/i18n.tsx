const defaultContext: LanguageContextValue = {
  locale: DEFAULT_LOCALE,
  setLocale: () => undefined,
  t: (key, params) => translate(DEFAULT_LOCALE, key, params),
};

const LanguageContext = createContext<LanguageContextValue>(defaultContext);

export function LanguageProvider({ children, initialLocale }: { children: ReactNode; initialLocale?: AppLocale }) {
  const [locale, setLocaleState] = useState<AppLocale>(() => {
    const startingLocale = normalizeLocale(initialLocale ?? readStoredLanguage());
    setFormatLocale(startingLocale);
    return startingLocale;
  });

  useEffect(() => {
    setFormatLocale(locale);
    document.documentElement.lang = locale === 'cs-CZ' ? 'cs' : 'en';
    try {
      if (typeof window.localStorage?.setItem === 'function') {
        window.localStorage.setItem(LANGUAGE_STORAGE_KEY, locale);
      }
    } catch {
      // Ignore storage errors in restricted browser/test contexts.
    }
  }, [locale]);

  const value = useMemo<LanguageContextValue>(
    () => ({
      locale,
      setLocale: (nextLocale: string) => {
        const normalizedLocale = normalizeLocale(nextLocale);
        setFormatLocale(normalizedLocale);
        setLocaleState(normalizedLocale);
      },
      t: (key, params) => translate(locale, key, params),
    }),
    [locale],
  );

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>;
}

export function useTranslation() {
  return useContext(LanguageContext);
}

export function normalizeLocale(locale: string | null | undefined): AppLocale {
  if (locale?.toLowerCase().startsWith('en')) {
    return 'en-US';
  }
  return DEFAULT_LOCALE;
}

export function translate(locale: AppLocale, key: string, params?: TranslationParams) {
  const template = translations[locale][key] ?? translations[DEFAULT_LOCALE][key] ?? key;
  if (!params) {
    return template;
  }
  return template.replace(/\{(\w+)\}/g, (_, paramKey: string) => String(params[paramKey] ?? ''));
}

export function enumLabel(t: (key: string) => string, namespace: string, value: string | null | undefined) {
  if (!value) {
    return t('common.unknown');
  }
  return t(`${namespace}.${value}`);
}

function readStoredLanguage() {
  if (typeof window === 'undefined') {
    return null;
  }
  try {
    if (typeof window.localStorage?.getItem !== 'function') {
      return null;
    }
    return window.localStorage.getItem(LANGUAGE_STORAGE_KEY);
  } catch {
    return null;
  }
}
